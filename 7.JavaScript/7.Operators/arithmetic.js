// We use arithmetic operators to use arithmetic operations such as addition , subtraction, etc.

let x = 5;

// addition operator
console.log("Addition: x + 3 = ", x + 3);

// subtraction operator
console.log("Subtraction: x - 3 =", x - 3);

// multiplication operator
console.log("Multiplication: x * 3 =", x * 3);

// division operator
console.log("Division: x / 3 =", x / 3);

// remainder operator
console.log("Remainder: x % 3 =", x % 3);

// Pre-increment operator
console.log("Increment: ++x =", ++x);

// Pre-decrement operator
console.log("Decrement: --x =", --x);

// exponentiation operator
console.log("Exponentiation: x ** 3 =", x ** 3);

// ++ and -- operators as prefix and postfix
// If we use ++ or -- as prefix such as ++num then the value of num is incremented by 1 and it returns the
// value. If we use it as postfix such as num++ then the original value is first returned and then the value is 
// incremented by 1.  

let var1 = 5 , var2 = 5;
// var1 is incremented by 1 and then it is displayed.
console.log(++var1)
// var2 displays original value first and then is incremented by 1.
console.log(var2++)
// Displays var2 incremented value
console.log(var2)

