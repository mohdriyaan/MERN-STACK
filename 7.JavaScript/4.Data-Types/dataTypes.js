// Data Types are of 2 types:-

// 1. Primitive Data Types:- They can hold a single simple value and are immutable. Immutable means they cannot be changed. Primitive data is stored in stack.
// They are further classified into 7 dat types:-
/*
    1.String
    2.Number
    3.Boolean
    4.Symbol
    5.Bigint
    6.null
    7.undefined
*/

// 2. Non Primitive Data Types:-They can hold multiple values and are mutable. Non-Primitive Data is stored in heap but pointers (address of the array elements) are stored in stack.
// They are further classified into 2 types:-
/*
    1.Objects
    2.Arrays
*/

